/**
 * 將 ACRI/VRI 分數轉為強度標籤
 * @param {number} score - 0.0 ~ 1.0
 * @returns {'low'|'medium'|'high'|'critical'}
 */
function scoreToIntensity(score) {
  if (score >= 0.85) return 'critical';
  if (score >= 0.7) return 'high';
  if (score >= 0.3) return 'medium';
  return 'low';
}

/**
 * 生成 Structured Hand-off 通知
 *
 * @param {Object} params
 * @param {string} params.pattern - Pattern 代碼（如 'MB'）
 * @param {number} params.score - ACRI 或 VRI 分數
 * @param {string} params.channel - 'push' 或 'vacuum'
 * @param {string} [params.locale] - 語言（'en'/'zh-trad'/'zh-simp'），預設 'en'
 * @param {Array}  [params.resources] - 節點自訂的資源連結
 * @param {Object} [params.customTemplate] - 節點自訂模板（覆蓋預設）
 * @returns {Object} 格式化的 Hand-off 通知
 */
function generate(params) {
  const {
    pattern,
    score,
    channel,
    locale = 'en',
    resources = [],
    customTemplate = null
  } = params;

  // ---- 驗證 ----

  if (!pattern || !PATTERN_DISPLAY_NAMES[pattern]) {
    throw new Error(`Invalid pattern: ${pattern}`);
  }

  if (typeof score !== 'number' || score < 0 || score > 1) {
    throw new Error(`Invalid score: ${score}. Must be 0.0 ~ 1.0`);
  }

  if (!['push', 'vacuum'].includes(channel)) {
    throw new Error(`Invalid channel: ${channel}. Must be 'push' or 'vacuum'`);
  }

  // ---- 選擇模板 ----

  const template = customTemplate || DEFAULT_TEMPLATES[locale] || DEFAULT_TEMPLATES['en'];

  // ---- 替換變數 ----

  const langKey = locale.startsWith('zh') ? 'zh' : 'en';
  const patternName = PATTERN_DISPLAY_NAMES[pattern][langKey];
  const intensityKey = scoreToIntensity(score);
  const intensityLabels = INTENSITY_LABELS[locale] || INTENSITY_LABELS['en'];
  const intensityLevel = intensityLabels[intensityKey];

  const renderedBody = template.body.map(line =>
    line
      .replace('{pattern_name}', patternName)
      .replace('{intensity_level}', intensityLevel)
  );

  // ---- 構建輸出 ----

  const handoff = {
    type: 'structured_handoff',
    generated_at: new Date().toISOString(),
    locale,
    channel,
    pattern,
    score: parseFloat(score.toFixed(4)),
    intensity: intensityKey,
    content: {
      header: template.header,
      body: renderedBody,
      disclaimer: template.disclaimer
    },
    // 語義脫敏：不含原始內容，只有 Pattern + 強度
    desensitized: true
  };

  // ---- 附加資源（節點自訂）----

  if (resources.length > 0) {
    handoff.content.resources_header = template.resources_header;
    handoff.content.resources = resources;
  }

  return handoff;
}

/**
 * 驗證自訂模板是否符合最低規格
 * 規格：必須包含 header, body (array), disclaimer
 *
 * @param {Object} template - 自訂模板
 * @returns {{ valid: boolean, errors: string[] }}
 */
function validateTemplate(template) {
  const errors = [];

  if (!template || typeof template !== 'object') {
    return { valid: false, errors: ['Template must be a non-null object'] };
  }

  if (typeof template.header !== 'string' || template.header.length === 0) {
    errors.push('Template must have a non-empty "header" string');
  }

  if (!Array.isArray(template.body) || template.body.length === 0) {
    errors.push('Template must have a non-empty "body" array');
  }

  if (typeof template.disclaimer !== 'string' || template.disclaimer.length === 0) {
    errors.push('Template must have a non-empty "disclaimer" string');
  }

  // 檢查 body 是否包含必要佔位符
  if (Array.isArray(template.body)) {
    const bodyText = template.body.join(' ');
    if (!bodyText.includes('{pattern_name}')) {
      errors.push('Template body must contain {pattern_name} placeholder');
    }
    if (!bodyText.includes('{intensity_level}')) {
      errors.push('Template body must contain {intensity_level} placeholder');
    }
  }

  return { valid: errors.length === 0, errors };
}

